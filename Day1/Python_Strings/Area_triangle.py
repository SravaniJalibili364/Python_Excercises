'''Calculate area of a triangle using base, height.'''

Base_of_the_triangle = int(input())
Height_of_the_triangle = int(input())

# using the formulae we are finding the area of the triangle(1/2*b*h)
Area_of_the_triangle = (1/2)*(Base_of_the_triangle*Height_of_the_triangle)
print(Area_of_the_triangle)